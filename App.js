class MainMenuToggle extends React.Component {
  constructor(props){
    super(props);
    this.state = {isToggleOn: false};
    this.handleClick = this.handleClick.bind(this);
  }

  handleClick(){
    this.setState(state => ({
      isToggleOn: !state.isToggleOn
    }));
    if(this.state.isToggleOn){
      alert("Toggle on");
    }
  }

  render(){
    return(
    <TouchableOpacity
        onPress={this.handleClick}>
        <Ionicons name="md-menu" size={32} color="#1b1b32" style={{marginRight: 30,}} />
      </TouchableOpacity>
    );
  }
}

